<template>
  <!-- <div class="login-wrap" style="background-image: url(../../assets/login-bg.jpg)"> -->
  <div class="login-wrap" style="background-image: url(./static/img/login-bg.jpg);">
    <div class="ms-login">
      <div class="ms-title">后台管理系统</div>
      <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="ms-content">
        <el-form-item prop="username">
          <el-input v-model="ruleForm.username" placeholder="username">
            <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
          </el-input>
        </el-form-item>
        <el-form-item prop="password">
          <el-input
            type="password"
            placeholder="password"
            v-model="ruleForm.password"
            @keyup.enter.native="submitForm('ruleForm')"
          >
            <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
          </el-input>
        </el-form-item>
        <div class="login-btn">
          <el-button type="primary" :plain="true" @click="submitForm('ruleForm')">登录</el-button>
        </div>
      </el-form>
    </div>
  </div>
</template>

<script>
// import store from "../../vuex/vuex.js";
export default {
  data: function() {
    return {
      ruleForm: {
        username: "",
        password: ""
      },
      rules: {
        username: [
          { required: true, message: "请输入用户名", trigger: "blur" }
        ],
        password: [{ required: true, message: "请输入密码", trigger: "blur" }]
      }
    };
  },
  methods: {
    submitForm(formName) {
      this.$refs[formName].validate(valid => {
        if (valid) {
          this.$axios
            .post(
              "http://192.168.12.11:8401/management/sysAccount/login",
              this.$qs.stringify({
                loginAccount: this.ruleForm.username,
                loginPassword: this.ruleForm.password
              })
            )
            .then(res => {
              console.log(res)
              if (res.status == 200) {
                if (res.data.code == 200) {
                  sessionStorage.setItem("token",res.data.data.token);
                  sessionStorage.setItem("userName",res.data.data.sysAccount.loginAccount);
                  sessionStorage.setItem("id",res.data.data.sysAccount.id);
                  this.$router.push("/");
                }else{
                  this.$message.error('账号或密码错误!');
                }
              }else{
                 this.$message.error('服务器链接失败');
              }
            });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    }
  }
};
</script>

